package javascript01t;

public class Member {
  int no;
  String email;
  String password;
  String tel;
  
  public int getNo() {
    return no;
  }
  public Member setNo(int no) {
    this.no = no;
    return this;
  }
  public String getEmail() {
    return email;
    return this;
  }
  public Member setEmail(String email) {
    this.email = email;
    return this;
  }
  public String getPassword() {
    return password;
  }
  public Member setPassword(String password) {
    this.password = password;
    return this;
  }
  public String getTel() {
    return tel;
  }
  public Member setTel(String tel) {
    this.tel = tel;
    return this;
  }
  
  
}
